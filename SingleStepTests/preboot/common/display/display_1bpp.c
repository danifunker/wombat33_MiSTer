/* Framebuffer row stride in bytes.
 *
 * Fixed builds (-DROW_BYTES=NN via VIDEO_VARIANT): compile-time
 * constant — mdc824/toby=80, m2hires=128, v8 (LC/LC II)=1024,
 * vasp (IIvi built-in)=2048.
 *
 * Auto build (-DROW_BYTES_AUTO): read the ROM's ScrnRow low-mem
 * global ($0106, word) the first time we paint. The ROM video driver
 * sets ScrnBase ($0824) and ScrnRow before the boot block runs (the
 * Happy Mac is painted with them), so by payload time both are live.
 * Implausible values fall back to 80 so a bad read degrades to the
 * legacy NuBus layout instead of scribbling. */
#ifdef ROW_BYTES_AUTO
static u32 g_row_bytes = 0;
u32 display_row_bytes(void)
{
    if (g_row_bytes == 0) {
        u32 v = *(volatile u16 *)0x0106;          /* ScrnRow */
        if (v < 16 || v > 4096 || (v & 3) != 0) v = 80;
        g_row_bytes = v;
    }
    return g_row_bytes;
}
#else
u32 display_row_bytes(void) { return (u32)ROW_BYTES; }
#endif

/* Paint one glyph at framebuffer byte ptr fb_ptr.
 * Inverts so 1-bits in font = 0 (white) on screen. */
void paint_glyph_at(u8 *fb_ptr, char c)
{
    int row;
    const u8 *g;
    u32 stride = display_row_bytes();
    if ((u8)c < 0x20 || (u8)c > 0x7E) c = '?';
    g = &g_font[((u8)c - 0x20) * 8];
    for (row = 0; row < 8; row++) {
        fb_ptr[row * stride] = (u8) ~(*g++);
    }
}

/* Paint a string at (row, col_byte). col_byte is the byte column on
 * the framebuffer (8 pixels per byte). Stops at NUL or after max
 * characters (= bytes), whichever first. */
void paint_string(u32 row, u32 col_byte, const char *s, u32 max_chars)
{
    u32 fb = fb_base();
    u8 *p;
    u32 n;
    if (!fb_ok(fb)) return;
    p = (u8 *)fb + row * display_row_bytes() + col_byte;
    for (n = 0; n < max_chars && *s; n++) {
        paint_glyph_at(p, *s++);
        p += 1;
    }
    /* clear any leftover positions up to max_chars */
    for (; n < max_chars; n++) {
        paint_glyph_at(p, ' ');
        p += 1;
    }
}

/* Fill `rows` framebuffer rows with 1bpp black (0xFF). Stride-aware
 * replacement for the old hardcoded 640x480 wipe. 480 rows is safe on
 * every supported device: mdc824 480*80=37.5K, V8 480*1024=480K
 * (within 512K VRAM), VASP 480*2048=960K (within 1MB VRAM). */
void display_wipe(u32 rows)
{
    u32 fb = fb_base();
    u32 *p = (u32 *)fb;
    u32 i, longs = rows * display_row_bytes() / 4;
    if (!fb_ok(fb)) return;
    for (i = 0; i < longs; i++) *p++ = 0xFFFFFFFF;
}
