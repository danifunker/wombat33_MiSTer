/* Paint one 8x8 glyph at byte ptr fb_ptr (top-left), using full
 * 8x8 = 64 framebuffer bytes (one per pixel). */
static void paint_glyph_at_byte(u8 *fb_ptr, char c)
{
    int row, col;
    const u8 *g;
    if ((u8)c < 0x20 || (u8)c > 0x7E) c = '?';
    g = &g_font[((u8)c - 0x20) * 8];
    for (row = 0; row < 8; row++) {
        u8 bits = *g++;
        u8 *line = fb_ptr + row * ROW_BYTES;
        for (col = 0; col < 8; col++) {
            line[col] = (bits & (0x80 >> col)) ? PX_BLACK : PX_WHITE;
        }
    }
}

/* Paint a string. (row, char_col) are in screen rows and 8-pixel
 * character cells respectively — same API as the 1bpp font_ascii.c
 * so bench_main.c is unchanged across video targets. */
void paint_string(u32 row, u32 char_col, const char *s, u32 max_chars)
{
    u32 fb = *(u32 *)0x0824;
    u8 *p;
    u32 n;
    if (fb == 0 || fb < 0x00100000) return;
    p = (u8 *)fb + row * ROW_BYTES + char_col * 8;
    for (n = 0; n < max_chars && *s; n++) {
        paint_glyph_at_byte(p, *s++);
        p += 8;
    }
    for (; n < max_chars; n++) {
        paint_glyph_at_byte(p, ' ');
        p += 8;
    }
}
