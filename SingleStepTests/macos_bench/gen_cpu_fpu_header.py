#!/usr/bin/env python3
"""Convert a CPU+FPU integration corpus JSON into a compact C header that
the Mac CPU+FPU benches (Mac OS APPL and preboot supervisor) include
verbatim.

Schema in the JSON (per item):
    name, op_a, program (list of u8), result_reg (0..7), expected (i32)

Usage:
    gen_cpu_fpu_header.py [corpus.json] [out.h]

Defaults to cpu_fpu/fpu_corpus_baseline.json -> cpu_fpu_tests.h. Any corpus
in the same schema works (e.g. cpu_fpu/save_restore_corpus.json ->
save_restore_tests.h); the emitted struct/symbol names are identical so a
bench can swap which header it includes without code changes.
"""

import json
import sys
import os

HERE = os.path.dirname(os.path.abspath(__file__))
CORPUS = os.path.join(HERE, "..", "cpu_fpu", "fpu_corpus_baseline.json")
OUT    = os.path.join(HERE, "cpu_fpu_tests.h")


def main():
    corpus = sys.argv[1] if len(sys.argv) > 1 else CORPUS
    out_path = sys.argv[2] if len(sys.argv) > 2 else OUT
    with open(corpus) as f:
        tests = json.load(f)

    max_prog = max(len(t["program"]) for t in tests)
    with open(out_path, "w") as out:
        out.write(f"/* Auto-generated by gen_cpu_fpu_header.py from\n"
                  f" *   {os.path.relpath(corpus, os.path.join(HERE, '..', '..'))}\n"
                  f" * {len(tests)} CPU+FPU integration tests.\n"
                  f" *\n"
                  f" * Each test loads op_a into D0, jsrs into program[], then\n"
                  f" * reads result_reg from the D-register file. The bench\n"
                  f" * checks D[result_reg] == expected (as a signed long).\n"
                  f" */\n")
        out.write(f"#ifndef CPU_FPU_TESTS_H\n#define CPU_FPU_TESTS_H\n\n")
        out.write(f"#define CPU_FPU_MAX_PROG  {max_prog}\n")
        out.write(f"#define CPU_FPU_N_TESTS   {len(tests)}\n\n")
        out.write(
            "typedef struct {\n"
            "    const char *name;\n"
            "    long          op_a;        /* input value */\n"
            "    unsigned char program[CPU_FPU_MAX_PROG];\n"
            "    unsigned short program_len;\n"
            "    unsigned char result_reg;  /* D-register to read after the program */\n"
            "    long          expected;    /* value we should find there */\n"
            "} CpuFpuTestSpec;\n\n"
            "static const CpuFpuTestSpec g_cpu_fpu_tests[] = {\n"
        )
        for t in tests:
            name = t["name"].replace('"', '\\"').replace('\\', '\\\\')
            prog = ",".join(str(b) for b in t["program"])
            out.write(f'    {{"{name}", {t["op_a"]}L,\n')
            out.write(f'      {{{prog}}}, {len(t["program"])}, {t["result_reg"]}, {t["expected"]}L}},\n')
        out.write("};\n\n#endif\n")
    print(f"wrote {out_path} ({len(tests)} tests, max_prog={max_prog})")


if __name__ == "__main__":
    main()
